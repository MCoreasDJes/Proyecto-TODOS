    void llenarComboEmpleado(){
        TodosLogica tdo = new TodosLogica();
        ArrayList<EmpleadosDAO> lstEmpleado = tdo.getEmpleados();
        
        for(EmpleadosDAO EMPDAORegistro : lstEmpleado){
            
            
            
            cmbEmpleados.addItem(EMPDAORegistro.getNombres()+" "+ EMPDAORegistro.getApellidos());
        }
    }
    
    void llenarComboDepa(){
        TodosLogica tdo = new TodosLogica();
        ArrayList<DepartamentosDAO> lstDepa = tdo.getDepartamentos();
        
        for(DepartamentosDAO EMPDAORegistro : lstDepa){
            
            
            
            cmbDepartamento.addItem(EMPDAORegistro.getNombre());
        }
    }
    
    void LLenarprioridades(){
        TodosLogica tdo = new TodosLogica();
        String PrioridadesBajas = tdo.PrioridadesBajas();
        lblBajas.setText(PrioridadesBajas);
        
        String PrioridadesInter = tdo.PrioridadIntermedia();
        lblInte.setText(PrioridadesInter);
        
        String PrioridadAlta = tdo.PrioridadAlta();
        lblPrioAlt.setText(PrioridadAlta);
    }
    
