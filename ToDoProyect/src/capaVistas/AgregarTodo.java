    public void llenarListado(){

        dibujo = new DefaultTableModel();
        dibujo.addColumn("ID");
        dibujo.addColumn("Empleado");
        dibujo.addColumn("Departamento");
        dibujo.addColumn("Tarea");
        dibujo.addColumn("Fecha Inicio");
        dibujo.addColumn("Fecha Fin");
        dibujo.addColumn("Prioridad");
        dibujo.addColumn("Index");

        jTable1.setModel(dibujo);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
        
        
        jTable1.getColumnModel().getColumn(7).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(7).setMinWidth(0);
        
        jTable1.getTableHeader().getColumnModel().getColumn(7).setMaxWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(7).setMinWidth(0);
        TodosLogica tdo = new TodosLogica();
        ArrayList<todoDAO> lstUsuarios = tdo.getTodoList();
        
        for(todoDAO todo : lstUsuarios){
            
            Vector registro = new Vector();
            
            registro.add(todo.getId());
            registro.add(todo.getNombres()+" "+todo.getApellidos());
            registro.add(todo.getNombre_departamento());
            registro.add(todo.getTarea());
            registro.add(todo.getFecha_inicio());
            registro.add(todo.getFecha_fin());
            String Prioridad = ValidarPrioridad(todo.getPrioridad());
            registro.add(Prioridad);
            registro.add(todo.getDepartamento());
            dibujo.addRow(registro);
        }
                

        
    }
    
    String ValidarPrioridad(int prio){
        String Prioridad ="";
        if (prio == 1) {
            Prioridad = "Baja";
        }else if (prio ==2) {
            Prioridad = "Intermedia";
        }else if (rootPaneCheckingEnabled) {
            Prioridad = "Alta";
        }
   
        return Prioridad;
    }
    
    
