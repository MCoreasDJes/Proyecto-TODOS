    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (EstaEditando == false) {
            TodosLogica tdo = new TodosLogica();
            todoDAO todoDAO = new todoDAO();
            Date dt = new Date();
            int cmb = cmbEmpleados.getSelectedIndex();
            int dep = cmbDepartamento.getSelectedIndex();
            String tarea = txtTarea.getText();
            Date dtInit = dtInicio.getDate();
            Date dtFinal = dtFin.getDate();
            int prioridad = cmbPrioridad.getSelectedIndex();

            //Formateando fechas
            try {
                DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
                String strDate = dateFormat.format(dtInit);
                String strDateFin = dateFormat.format(dtFinal);

                todoDAO.setEmpleado(cmb);
                todoDAO.setDepartamento(dep);
                todoDAO.setFecha_inicio(strDate);
                todoDAO.setFecha_fin(strDateFin);
                todoDAO.setPrioridad(prioridad);
                todoDAO.setTarea(tarea);

                tdo.LLenarData(todoDAO);
                llenarListado();
                Limpiar();
                LLenarprioridades();
            }catch(Exception e){System.out.println("Error de parseo");}
        }else {
            TodosLogica tdo = new TodosLogica();
            todoDAO todoDAO = new todoDAO();
            Date dt = new Date();
            int index = jTable1.getSelectedRow();
            int id = (int)jTable1.getValueAt(index, 0);
            int cmb = cmbEmpleados.getSelectedIndex();
            int dep = cmbDepartamento.getSelectedIndex();
            String tarea = txtTarea.getText();
            Date dtInit = dtInicio.getDate();
            Date dtFinal = dtFin.getDate();
            int prioridad = cmbPrioridad.getSelectedIndex();

            //Formateando fechas
            try {
                DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
                String strDate = dateFormat.format(dtInit);
                String strDateFin = dateFormat.format(dtFinal);
                todoDAO.setId(id);
                todoDAO.setEmpleado(cmb);
                todoDAO.setDepartamento(dep);
                todoDAO.setFecha_inicio(strDate);
                todoDAO.setFecha_fin(strDateFin);
                todoDAO.setPrioridad(prioridad);
                todoDAO.setTarea(tarea);

                tdo.PrepararDataEdit(todoDAO);
                llenarListado();
                Limpiar();
                LLenarprioridades();
                JOptionPane.showMessageDialog(null, "Tarea  Editada");
            }catch(Exception e){System.out.println("Error de parseo");}
        
        }
        
        
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

